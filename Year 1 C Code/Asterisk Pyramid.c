#include <stdio.h>

int main()
{
    // This code prints asterisks in a pyramid pattern
    
    // Here we print the asterisks with tabs between and a newline after
    printf("\t\t\t*\n");
    printf("\t\t*\t\t*\n");
    printf("\t*\t\t*\t\t*\n");
    printf("*\t\t*\t\t*\t\t*\n");
    
    return 0;
}
