#include <stdio.h>

int main()
{
    /*This code prints a series of asterisks in a descending
    staircase pattern*/
    
    // Here we print the asterisks with tabs between and new lines afterwards
    printf("*\n");
    printf("*\t*\n");
    printf("*\t*\t*\n");
    printf("*\t*\t*\t*\n");
    return 0;
}
