#include <stdio.h>

int main()
{
    /* This code writes "Hello World", then "Goodbye World" on a separate line 
    before taking a character that the user types in*/
    
    // First we print the two lines
    printf("Hello World\n");
    printf("Goodbye World\n");
    
    /* Getchar is used to hold the program so the user can see the display */
    getchar();
    return 0;
}
